#include "swoole.h"
#include <string>

namespace swoole
{

class Exception
{
public:
    int code;

    Exception(enum swErrorCode _code)
    {
        code = _code;
    }
};

}
;

void swoole_throw_error(enum swErrorCode code)
{
    throw swoole::Exception(code);
}
